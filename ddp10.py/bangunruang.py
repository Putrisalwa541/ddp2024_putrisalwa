import math

def l_balok(p,l,t):
    hitung = 2*(p*l)+(p*t)+(l*t)
    print(f"Luas Balok adalah {hitung}")
    
def l_kubus(s):
    hitung = 6*(s*s)
    print(f"Luas Kubus adalah {hitung}")
    
def l_tabung(r,t):
    hitung = 2*3.14*r*t
    print(f"Luas Tabung adalah {hitung}")
    
def l_limas_segitiga(a,t,st):
    hitung = (1/2*a+t)+(3*st)
    print(f"Luas Limas Segitiga adalah {hitung}")
    
def l_prima_segitiga(a,t,k):
    hitung = (2*1/2*a*t)+(k*t)
    print(f"Luas Prima Segitiga adalah {hitung}")
    
    
    
        