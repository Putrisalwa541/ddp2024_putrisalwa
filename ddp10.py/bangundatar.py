import math
def l_persegi(sisi):
    hitung = sisi*sisi
    print(f"Luas Persegi adalah {hitung}")
    
def l_persegi_panjang(p,l):
    hitung = p*l
    print(f"Luas Pesergi Panjang adalah {hitung}")
    
def l_segitiga(a,t):
    hitung = 1/2*a*t
    print(f"Luas Persegi Panjang adalah {hitung}")
    
def l_lingkaran(r):
    hitung = r*3.14
    print(f"Luas Lingkaran adalah {hitung}")
    
